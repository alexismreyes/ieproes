    // Validate form
    protected function validateForm()
    {
        global $Language, $Security;

        // Check if validation required
        if (!Config("SERVER_VALIDATE")) {
            return true;
        }
        $validateForm = true;
        if ($this->nombre_evaluacion->Required) {
            if (!$this->nombre_evaluacion->IsDetailKey && EmptyValue($this->nombre_evaluacion->FormValue)) {
                $this->nombre_evaluacion->addErrorMessage(str_replace("%s", $this->nombre_evaluacion->caption(), $this->nombre_evaluacion->RequiredErrorMessage));
            }
        }
        if ($this->descripcion_evaluacion->Required) {
            if (!$this->descripcion_evaluacion->IsDetailKey && EmptyValue($this->descripcion_evaluacion->FormValue)) {
                $this->descripcion_evaluacion->addErrorMessage(str_replace("%s", $this->descripcion_evaluacion->caption(), $this->descripcion_evaluacion->RequiredErrorMessage));
            }
        }

        // Return validate result
        $validateForm = $validateForm && !$this->hasInvalidFields();

        // Call Form_CustomValidate event
        $formCustomError = "";
        $validateForm = $validateForm && $this->formCustomValidate($formCustomError);
        if ($formCustomError != "") {
            $this->setFailureMessage($formCustomError);
        }
        return $validateForm;
    }

